PROMPT 3: AFFICHER FACTURATION 
PROMPT 4: FAIRE UN RABAIS
PROMPT 5: MISES A JOUR ET SUPPRESSION 
PROMPT 6: QUITTER
ACCEPT selection PROMPT " Entrer option 1-6: "
SET TERM OFF
COLUMN script NEW_VALUE v_script
SELECT CASE '&selection'
WHEN '1' THEN 'VENTELIBRE.SQL'
